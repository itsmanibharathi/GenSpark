# Conditional

# if statement
x = 10
if x > 5:
    print("x is greater than 5")

# if else statement
if x > 5:
    print("x is greater than 5")
else:
    print("x is less than 5")

# if elif else statement

x=5

if x > 5:
    print("x is greater than 5")
elif x == 5:
    print("x is equal to 5")
else:
    print("x is less than 5")


# Short Hand If
if x > 5: print("x is greater than 5")
